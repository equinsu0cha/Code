#include "lcd_stm32f0.h"
#include "stm32f0xx.h"
#include <stdio.h>
//====================================================================
// GLOBAL CONSTANTS
//====================================================================

//====================================================================
// GLOBAL VARIABLES
//====================================================================
char lcdstring[16];
uint32_t temp;
//====================================================================
// FUNCTION DECLARATIONS
//====================================================================


//====================================================================
// MAIN FUNCTION
//====================================================================
void main (void)
{
	init_LCD();								// Initialise lcd
	lcd_putstring("EEE3017W Prac 6");		// Display string on line 1
	lcd_command(LINE_TWO);					// Move cursor to line 2
	temp = (RCC->CFGR);
	sprintf(lcdstring,"%d",temp);
	lcd_putstring(lcdstring);				// Display string on line 2
	for(;;);								// Loop forever
}											// End of main

